  NUMERO NUMBER 
, OPERACION VARCHAR2(1) 
, NOMBRE VARCHAR2(80) 
, DIRECCION VARCHAR2(100) 
, CIUDAD VARCHAR2(60) 
, FONO VARCHAR2(30) 
, SALDO NUMBER 
);

