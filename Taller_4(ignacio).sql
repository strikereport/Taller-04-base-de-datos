-- Tabla    CUENTA

CREATE TABLE CUENTA 
(
  NUMERO NUMBER NOT NULL 
, NOMBRE VARCHAR2(80) 
, DIRECCION VARCHAR2(100) 
, CIUDAD VARCHAR2(60) 
, FONO VARCHAR2(30) 
, SALDO NUMBER 
, CONSTRAINT CUENTA_PK PRIMARY KEY 
  (
    NUMERO 
  )
  ENABLE 
);

-- Tabla MOVIMIENTO

CREATE TABLE MOVIMIENTO 
